#include <stdlib.h>
#include "3-calc.h"

/**
 * op_add - add
 *
 * @a: int
 * @b: int
 *
 * description: add two integer.
 *
 * Return: Result
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub
 *
 * @a: int
 * @b: int
 *
 * description: subtract two integer.
 *
 * Return: Result
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - mul
 *
 * @a: int
 * @b: int
 *
 * description: multiple two integer.
 *
 * Return: Result
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - div
 *
 * @a: int
 * @b: int
 *
 * description: divise two integer.
 *
 * Return: Result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod
 *
 * @a: int
 * @b: int
 *
 * description: modulo two integer.
 *
 * Return: Result
 */

int op_mod(int a, int b)
{
	return (a % b);
}
